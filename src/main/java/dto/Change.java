package dto;

public class Change {
    public double pennies;
    public double nickels;
    public double dimes;
    public double quarters;

    public Change(){

    }

    public double getPennies() {

        return pennies;
    }

    public void setPennies(double pennies) {

        this.pennies = pennies;
    }

    public double getNickels() {

        return nickels;
    }

    public void setNickles(double nickels) {
        this.nickels = nickels;
    }

    public double getDimes() {
        return dimes;
    }

    public void setDimes(double dimes) {
        this.dimes = dimes;
    }

    public double getQuarters() {

        return quarters;
    }

    public void setQuarters(double quarters) {
        this.quarters = quarters;
    }


}
